package Exercise17;

public class Date {
	private int day;
	private int month;
	private int year;
/**
 * 
 *this is a Date constructor
 * 
 *ex:
 * 	new Date(5,10,2023);
 * 	new Date(6,10,2023);
 * 	new Date(7,10,2023);
 * 	
 */
	public Date(int day, int month, int year) {
		this.day = day;
		this.month = month;
		this.year = year;
	}

}
