package Exercise17;

public class TemperatureRange {
	private int low;
	private int high;
/**
 * 
 * this is a TemperatureRange constructure
 * 
 * @param low
 * @param high
 * 
 * ex:
 *  new TemperatureRange(25,30);
 *  new TemperatureRange(23,32);
 *  new TemperatureRange(24,30);
 */
	public TemperatureRange(int low, int high) {
		this.low = low;
		this.high = high;
	}


}
