package Exercise18;

public class ClockTime {
	private int hour;
	private int minute;
/**
 * this is a constructor of ClockTime
 * 
 * @param hour
 * @param minute
 * ex:
 *  new ClockTime(7,30);
 *  new ClockTime(9,30);
 *  new ClockTime(11,30);
 *  
 */
	public ClockTime(int hour, int minute) {
		this.hour = hour;
		this.minute = minute;
	}


}
